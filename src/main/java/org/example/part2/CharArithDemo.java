package org.example.part2;

public class CharArithDemo {
    public static void main(String[] args){
        char ch;

        ch = 'X';
        System.out.println("ch содержит " + ch);
        ch++;
        System.out.println("ch теперь содержит " + ch);
        ch = 90;
        System.out.println("ch теперь содержит " + ch);
    }
}
